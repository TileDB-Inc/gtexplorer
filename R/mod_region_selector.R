
regionSelectorUI <- function(id) {
  ns <- NS(id)
  tagList(
    selectizeInput(
      inputId = ns("contig"),
      label = "Chromosome",
      choices = c(`Database not loaded...` = "")
    ),
    splitLayout(
      textInput("region_start", "Start", placeholder = "Region start"),
      textInput("region_end", "End", placeholder = "Region end")
    )
  )
}


# param contigs: vector of named chromosome lengths
regionSelectorServer <- function(id, contigs) {
  moduleServer(id, function(input, output, session) {

    observeEvent(contigs(), {
      req(contigs())
      cat("\n\nUpdating contigs in search box...\n", file = stderr())

      updateSelectizeInput(
        session,
        inputId = "contig",
        choices = names(contigs()),
        selected = c(Search = ""),
        server = TRUE,
        options = list(
          placeholder = "Enter chromosome",
          openOnFocus = FALSE
        )
      )
    })

    reactive({
      contigs()[input$contig]
    })

  })
}
